package com.example.smartchef.models;

public class Customer {

    String id, emaill, namee, age, gender, number, password;

    public Customer() {
    }

    public Customer(String id, String emaill, String namee, String age, String gender, String number, String password) {
        this.id = id;
        this.emaill = emaill;
        this.namee = namee;
        this.age = age;
        this.gender = gender;
        this.number = number;
        this.password = password;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getEmaill() {
        return emaill;
    }

    public void setEmaill(String emaill) {
        this.emaill = emaill;
    }

    public String getNamee() {
        return namee;
    }

    public void setNamee(String namee) {
        this.namee = namee;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
